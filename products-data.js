function getProductBySlug(slug) {
    // ุชุทุจูุน ุงูู slug ููุจุญุซ
    const normalizedSlug = decodeURIComponent(slug).trim();
    
    return window.productsData.find(product => {
        const productSlug = product.slug;
        
        // ูุทุงุจูุฉ ูุจุงุดุฑุฉ
        if (productSlug === normalizedSlug) return true;
        
        // ูุทุงุจูุฉ ูุฑูุฉ ููุนุฑุจูุฉ
        const normalizedProductSlug = productSlug.replace(/[\u064B-\u065F]/g, '').replace(/\s+/g, '-');
        const searchSlug = normalizedSlug.replace(/[\u064B-\u065F]/g, '').replace(/\s+/g, '-');
        
        return normalizedProductSlug === searchSlug;
    });
}

function getProductById(id) {
    return window.productsData.find(product => product.id === parseInt(id));
}

function getProductsByCategory(category) {
    return window.productsData.filter(product => product.category === category);
}

// Search functionality
function searchProducts(query) {
    const searchTerm = query.toLowerCase();
    return window.productsData.filter(product => 
        product.title.toLowerCase().includes(searchTerm) ||
        product.description.toLowerCase().includes(searchTerm) ||
        product.category.toLowerCase().includes(searchTerm)
    );
}

// Filter by price range
function filterByPriceRange(minPrice, maxPrice) {
    return window.productsData.filter(product => 
        product.salePrice >= minPrice && product.salePrice <= maxPrice
    );
}

// Get featured products (every 5th product)
function getFeaturedProducts() {
    return window.productsData.filter((product, index) => (index + 1) % 5 === 0);
}

